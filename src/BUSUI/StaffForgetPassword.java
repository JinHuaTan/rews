    private void jbtResetPWActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtResetPWActionPerformed
        stf.setPassword("12345678");
        staffControl.updateRecord(stf);
        JOptionPane.showMessageDialog(null, "Your Password has change to 12345678. Please change it in your next login in the P & C info.", "Reset Password Successfully", JOptionPane.INFORMATION_MESSAGE);
        dispose();
    }//GEN-LAST:event_jbtResetPWActionPerformed

    private void jbtCheckSQActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtCheckSQActionPerformed
        if (jtfCurrentSP.getText().equals(stf.getSecuritypass())) {
            resetPanel.setVisible(true);
            jlbStaffName.setText(stf.getStaffname());
            JOptionPane.showMessageDialog(null, "Click on Reset Button to rest your password to 12345678.", "Reset Password", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Invalid Security Answer ", "Wrong Answer", JOptionPane.ERROR_MESSAGE);
            jtfCurrentSP.setText("");
            closeProgram();
        }

    }//GEN-LAST:event_jbtCheckSQActionPerformed

    private void jbtCheckUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtCheckUserActionPerformed
        String id = jtfCurrentUsername.getText();
        stf = staffControl.selectRecordbyUsername(id);
        if (stf != null) {

            jtfCurrentSQ.setText(stf.getSecurityquestion());
            jtfCurrentUsername.setEnabled(false);
            checkUser = true;
            count = 0;
            jbtCheckUser.setVisible(false);
            jtfCurrentSP.setEnabled(true);
            jbtCheckSQ.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Invalid user ID ", "STAFF NOT FOUND", JOptionPane.ERROR_MESSAGE);
            jtfCurrentUsername.setText("");
            closeProgram();
        }
    }//GEN-LAST:event_jbtCheckUserActionPerformed

