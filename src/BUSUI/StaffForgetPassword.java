    public void closeProgram() {

        count++;
        if (count == 3) {
            JOptionPane.showMessageDialog(null, "You've typed the invalid username or wrong answer for 3 times!!!", "Closing Program...", JOptionPane.ERROR_MESSAGE);
            System.exit(0);
        }
    }

